        <p>The Decree of Canopus is a bilingual inscription in two languages, and in three scripts. It was written in three writing systems: Egyptian hieroglyphs, Demotic, and Greek, on an ancient Egyptian memorial stone stele, the Stone of Canopus. The inscription is a decree by Egyptian priests honoring Pharaoh Ptolemy III Euergetes; Queen Berenice, his wife; and Princess Berenice in 238 BC.</p>
        <p>This is the earliest of the series of bilingual inscriptions of the "Rosetta Stone Series", the next being the Decree of Memphis, for Ptolemy IV, and the third, final stone, being the Memphis Decree Rosetta Stone, inscribed for Ptolemy V, in 196 BCE. Having a greater number of different hieroglyphs than the Rosetta Stone, the Canopus Stone has proved crucial in deciphering them. Two copies of the stele stones exist, in different line widths.</p>
       
