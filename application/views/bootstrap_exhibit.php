            <h4>Straight Pool</h4>
        <p class="lead">The championships of his era were contested in the game of 14.1 continuous ("straight pool"), but varied in format from contest to contest and were not annual events. Championships were challenge matches between two players often played over several days to relatively high numbers (1,500 points for example).</p>
        <p>He was one of the first three members inducted into the Billiard Congress of America's Hall of Fame, in 1966. He was ranked number 3 on the Billiards Digest 50 Greatest Players of the Century.</p>
        
